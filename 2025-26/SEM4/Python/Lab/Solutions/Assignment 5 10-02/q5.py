# 5. Write a Python program to demonstrate tuple packing and unpacking with appropriate examples.

student_info = ("User", 20, "Computer Science")
print("Packed Tuple:", student_info)

name, age, subject = student_info

print("Unpacked values:")
print("Name:", name)
print("Age:", age)
print("Subject:", subject)

# Output:
# Packed Tuple: ('User', 20, 'Computer Science')
# Unpacked values:
# Name: User
# Age: 20
# Subject: Computer Science
