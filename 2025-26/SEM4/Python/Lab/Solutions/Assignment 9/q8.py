# Write a Python program to simulate a dice roll using the random module.
import random  # Import the random module to generate random numbers
dice_roll = random.randint(1, 6)  # Simulate a dice roll by generating a random number between 1 and 6
print(f"You rolled a {dice_roll}.")  # Output: You rolled a 4 (or any number between 1 and 6 depending on the random roll)
