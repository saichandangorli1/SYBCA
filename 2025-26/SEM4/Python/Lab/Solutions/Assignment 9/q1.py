# Write a Python program to import the math module and display the square root and factorial
# of a given number.


import math
number = 5
sqrt_result = math.sqrt(number)
factorial_result = math.factorial(number)
print(f"The square root of {number} is: {sqrt_result}")  # Output: The square root of 5 is: 2.23606797749979
print(f"The factorial of {number} is: {factorial_result}")  # Output: The factorial of 5 is: 120

