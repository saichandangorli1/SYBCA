# Write a Python program to shuffle a list of numbers using random.shuffle().

import random  # Import the random module to use the shuffle function
numbers = [1, 2, 3, 4, 5] 
random.shuffle(numbers)  # Shuffle the list of numbers in place
print(f"The shuffled list of numbers is: {numbers}.")  # Output: The shuffled list of numbers is: [3, 1, 5, 2, 4] (or any random order of the numbers)

