# Write a Python program to select a random element from a list of
# colors using random.choice().
import random  # Import the random module to use the choice function
colors = ['red', 'blue', 'green', 'yellow', 'purple']  # Define a list of colors
random_color = random.choice(colors)  # Select a random color from the list
print(f"The randomly selected color is: {random_color}.")  # Output: The randomly
# selected color is: blue (or any color from the list depending on the random selection)