# 2. Write a Python function student_info(name, age, course) and call it
# using keyword arguments.

def student_info(name, age, course):
    print(f"Name: {name}")
    print(f"Age: {age}")
    print(f"Course: {course}")
student_info(name="Alice", age=20, course="Computer Science")
# output: Name: Alice
# output: Age: 20
# output: Course: Computer Science

